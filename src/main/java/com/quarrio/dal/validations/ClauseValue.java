package com.quarrio.dal.validations;

import java.util.List;

/**
 * @author Zain ul Hassan
 *
 */
public class ClauseValue {

	private List<String> firstPart;
	private List<String> secondPart;
	private List<String> thirdPart;
	private List<String> fourthPart;
	private List<String> fifthPart;
	private String lastPart;

	public List<String> getFirstPart() {
		return firstPart;
	}

	public void setFirstPart(List<String> firstPart) {
		this.firstPart = firstPart;
	}

	public List<String> getSecondPart() {
		return secondPart;
	}

	public void setSecondPart(List<String> secondPart) {
		this.secondPart = secondPart;
	}

	public List<String> getThirdPart() {
		return thirdPart;
	}

	public void setThirdPart(List<String> thirdPart) {
		this.thirdPart = thirdPart;
	}

	public List<String> getFourthPart() {
		return fourthPart;
	}

	public void setFourthPart(List<String> fourthPart) {
		this.fourthPart = fourthPart;
	}

	public List<String> getFifthPart() {
		return fifthPart;
	}

	public void setFifthPart(List<String> fifthPart) {
		this.fifthPart = fifthPart;
	}

	public String getLastPart() {
		return lastPart;
	}

	public void setLastPart(String lastPart) {
		this.lastPart = lastPart;
	}

}
